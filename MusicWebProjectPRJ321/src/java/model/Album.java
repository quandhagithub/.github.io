package model;

/**
 *
 * @author ADMIN
 */
public class Album {
    private String ID;
    private String title;
    private String genre;
    private String author;
    private String path;

    public Album() {
    }

    public Album(String ID, String title, String genre, String path,String author) {
        this.ID = ID;
        this.title = title;
        this.genre = genre;
        this.author = author;
        this.path = path;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
    
    
}
